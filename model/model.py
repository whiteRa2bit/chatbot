SOS_token = 0
EOS_token = 1

device = torch.device("cpu")

class Lang:
    def __init__(self, name):
        self.name = name
        self.word2index = {}
        self.word2count = {}
        self.index2word = {0: "SOS", 1: "EOS"}
        self.n_words = 2  # Count SOS and EOS

    def addSentence(self, sentence):
        for word in sentence.split(' '):
            self.addWord(word)

    def addWord(self, word):
        if word not in self.word2index:
            self.word2index[word] = self.n_words
            self.word2count[word] = 1
            self.index2word[self.n_words] = word
            self.n_words += 1
        else:
            self.word2count[word] += 1


# Turn a Unicode string to plain ASCII, thanks to
# https://stackoverflow.com/a/518232/2809427
def unicodeToAscii(s):
    return ''.join(
        c for c in unicodedata.normalize('NFD', s)
        if unicodedata.category(c) != 'Mn'
    )

# Lowercase, trim, and remove non-letter characters
def normalizeString(s):
    s = s.replace('?', ' ? ').replace('!', ' ! ').replace(':', '').replace('(', '').replace(')', '').replace('.', ' . ')
    s = unicodeToAscii(s.lower().strip())
    return s



def find_occurrences(s, ch):
    return [i for i, letter in enumerate(s) if letter == ch]


def readLangs():
    print("Reading lines...")

    f = open("./data/ru.conversations.txt", "r")
    data = f.read()

    strings = re.split(r'\n\n', data)

    pairs = []

    for dialog in strings:
        new_str_pos = find_occurrences(dialog, '\n')
        if len(new_str_pos) > 0:
            question = dialog[:new_str_pos[0]]

            if len(new_str_pos) > 1:
                answer = dialog[new_str_pos[0] + 1:new_str_pos[1]]
            else:
                answer = dialog[new_str_pos[0] + 1:]

            question = normalizeString(question[2:])
            answer = normalizeString(answer[2:])

            pairs.append([question, answer])

    input_lang = Lang('question')
    output_lang = Lang('answer')

    return input_lang, output_lang, pairs




MAX_LENGTH = 7


