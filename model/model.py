def modify_output(output_words):
    puncs = ['!', ',', '.', '?']
    output_sentence = ' '.join(output_words[:-1])
    output_sentence = output_sentence.replace(' ?', '?').replace(' !', '!').replace(' .', '.')
    res = ""
    if len(output_sentence) != 0:
        prev_sym = output_sentence[0]
        res += prev_sym
    else:
        prev_sym = ""
    for i in range(1, len(output_sentence)):
        cur_sym = output_sentence[i]
        if prev_sym in puncs and cur_sym in puncs:
            pass
        else:
            res += cur_sym
        prev_sym = cur_sym
    return res

